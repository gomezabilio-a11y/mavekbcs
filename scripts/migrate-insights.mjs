async function main() {
  const url = process.env.DATABASE_URL;
  if (!url) {
    console.error("DATABASE_URL not set");
    process.exit(1);
  }

  const conn = await mysql.createConnection(url);
  console.log("Connected to DB");

  let inserted = 0;
  let updated = 0;
  let errors = 0;

  for (const article of ARTICLES) {
    const imageUrl = THUMBNAIL_MAP[article.category] ?? null;
    const tags = JSON.stringify(article.tags ?? []);
    const relatedIndustries = JSON.stringify(article.relatedIndustries ?? []);
    const relatedSolutions = JSON.stringify(article.relatedSolutions ?? []);
    const publishedAt = new Date().toISOString().slice(0, 19).replace("T", " ");

    try {
      const [result] = await conn.execute(
        `INSERT INTO insights 
          (slug, title, titleKo, titleJa, excerpt, excerptKo, excerptJa, content, contentKo, contentJa, 
           category, tags, relatedIndustries, relatedSolutions, imageUrl, readTimeMinutes, featured, publishedAt)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
         ON DUPLICATE KEY UPDATE
           title = VALUES(title),
           titleKo = VALUES(titleKo),
           titleJa = VALUES(titleJa),
           excerpt = VALUES(excerpt),
           excerptKo = VALUES(excerptKo),
           excerptJa = VALUES(excerptJa),
           category = VALUES(category),
           tags = VALUES(tags),
           relatedIndustries = VALUES(relatedIndustries),
           relatedSolutions = VALUES(relatedSolutions),
           imageUrl = VALUES(imageUrl),
           readTimeMinutes = VALUES(readTimeMinutes),
           featured = VALUES(featured)`,
        [
          article.slug,
          article.title,
          article.titleKo ?? null,
          article.titleJa ?? null,
          article.excerpt ?? null,
          article.excerptKo ?? null,
          article.excerptJa ?? null,
          article.excerpt ?? null,  // content = excerpt as placeholder
          article.excerptKo ?? null,
          article.excerptJa ?? null,
          article.category ?? null,
          tags,
          relatedIndustries,
          relatedSolutions,
          imageUrl,
          article.readTimeMinutes ?? 5,
          article.featured ? 1 : 0,
          publishedAt,
        ]
      );

      const affectedRows = result.affectedRows;
      if (affectedRows === 1) {
        inserted++;
        console.log(`✓ Inserted: ${article.slug}`);
      } else if (affectedRows === 2) {
        updated++;
        console.log(`↻ Updated: ${article.slug}`);
      } else {
        console.log(`- No change: ${article.slug}`);
      }
    } catch (err) {
      errors++;
      console.error(`✗ Error for ${article.slug}:`, err.message);
    }
  }

  await conn.end();
  console.log(`\nMigration complete: ${inserted} inserted, ${updated} updated, ${errors} errors`);
  console.log(`Total articles processed: ${ARTICLES.length}`);
}

main().catch(console.error);
